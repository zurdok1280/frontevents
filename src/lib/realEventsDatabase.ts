export const getUniqueRadioStations = () => {
  const stations = [...new Set(realEventsDatabase.map(event => event.radioStation))];
  return stations.sort();
};

export const getUniqueCities = () => {
  const cities = [...new Set(realEventsDatabase.map(event => event.city))];
  return cities.sort();
};

export const getUniqueVenues = () => {
  const venues = [...new Set(realEventsDatabase.map(event => event.venue))];
  return venues.filter(v => v !== "Por confirmar").sort();
};

export const getUniqueGenres = () => {
  const genres = [...new Set(realEventsDatabase.map(event => event.genre))];
  return genres.sort();
};

export const filterEvents = (filters: {
  city?: string;
  radioStation?: string;
  venue?: string;
  genre?: string;
  startDate?: Date;
  endDate?: Date;
}) => {
  return realEventsDatabase.filter(event => {
    if (filters.city && event.city !== filters.city) return false;
    if (filters.radioStation && event.radioStation !== filters.radioStation) return false;
    if (filters.venue && event.venue !== filters.venue) return false;
    if (filters.genre && event.genre !== filters.genre) return false;
    
    if (filters.startDate || filters.endDate) {
      const eventDate = new Date(event.dateISO);
      if (filters.startDate && eventDate < filters.startDate) return false;
      if (filters.endDate && eventDate > filters.endDate) return false;
    }
    
    return true;
  });
};

export const getCityStats = () => {
  const cities = getUniqueCities();
  return cities.map(city => {
    const cityEvents = realEventsDatabase.filter(e => e.city === city);
    const totalSpots = cityEvents.reduce((sum, e) => sum + e.spots, 0);
    const totalMentions = cityEvents.reduce((sum, e) => sum + e.mentions, 0);
    const avgSpotLength = cityEvents.reduce((sum, e) => sum + e.spotLength, 0) / cityEvents.length;
    
    const genres = cityEvents.map(e => e.genre);
    const genreCounts = genres.reduce((acc, genre) => {
      acc[genre] = (acc[genre] || 0) + 1;
      return acc;
    }, {} as Record<string, number>);
    const topGenre = Object.entries(genreCounts).sort((a, b) => b[1] - a[1])[0]?.[0] || "";

    return {
      city,
      totalEvents: cityEvents.length,
      totalSpots,
      totalMentions,
      avgSpotLength: Math.round(avgSpotLength),
      totalReach: cityEvents[0]?.reach || "0",
      topGenre
    };
  });
};

export const getRadioStationStats = () => {
  const stations = getUniqueRadioStations();
  return stations.map(station => {
    const stationEvents = realEventsDatabase.filter(e => e.radioStation === station);
    const totalSpots = stationEvents.reduce((sum, e) => sum + e.spots, 0);
    const avgSpotLength = stationEvents.reduce((sum, e) => sum + e.spotLength, 0) / stationEvents.length;
    const cities = [...new Set(stationEvents.map(e => e.city))];
    
    return {
      station,
      totalEvents: stationEvents.length,
      totalSpots,
      avgSpotLength: Math.round(avgSpotLength),
      cities: cities.join(", ")
    };
  });
};
